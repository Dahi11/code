public void Encontrar_Blanca(){
    int i, j, x,y;
    boolean senial = false;
    x=0;
    y=0;
    i = 0;
    j = 0;
    while(i<3){
        while(j<3){
            if(matriz_juego[i][j].compareTo("*")==0){
                x=i;
                y=j;   
                senial= true;
                break;
            }
            else{
                j++;
            }
        }
        if(senial){
            break;
        }
        else {
            i++;
            j=0;
        }
    }
    Apagar_blanco(x,y);
}
